public class Infor {
    private String name;
    private String lv;

    public Infor() {

    }

    public Infor(String name, String lv) {
        this.name = name;
        this.lv = lv;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLv() {
        return lv;
    }

    public void setLv(String lv) {
        this.lv = lv;
    }


}
